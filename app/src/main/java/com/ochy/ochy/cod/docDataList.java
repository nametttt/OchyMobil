package com.ochy.ochy.cod;

public class docDataList {

    public String DocType;
    public  String DocFIO;


    public docDataList(String docType, String docFIO) {
        DocType = docType;
        DocFIO = docFIO;
    }

    public String getDocType() {
        return DocType;
    }

    public String getDocDocFIO() {
        return DocFIO;
    }

}
